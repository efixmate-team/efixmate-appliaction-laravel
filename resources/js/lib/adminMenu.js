export function groupedAdminMenu() {
  const grouped = adminMenu.reduce((groups, item) => {
    const key = item.section || 'Other';
    if (!groups[key]) groups[key] = [];
    groups[key].push(item);
    return groups;
  }, {});

  return Object.fromEntries(
    Object.entries(grouped)
      .sort(([a], [b]) => {
        const ai = SECTION_ORDER.includes(a) ? SECTION_ORDER.indexOf(a) : 999;
        const bi = SECTION_ORDER.includes(b) ? SECTION_ORDER.indexOf(b) : 999;
        return ai - bi || a.localeCompare(b);
      })
      .map(([section, items]) => [
        section,
        items.sort((a, b) => a.path.localeCompare(b.path)),
      ]),
  );
}